x = 3
y = 2
print (x == y) #Espera-se False pois X é maior que Y;
print (x > y) #Espera-se True pois X é maior;
print (x < y) #Espera-se False pois Y é menor;
soma = x + y #Espera-se somar X + Y para dar 5;
print (soma) #O resultado de X + Y;
print (soma > y) #True, pois é maior;
print (soma <= y) #Soma não é menor e nem igual;
print (soma >= y) #Soma é maior mas não é igual;
divisao = x / y #Dividir X por Y;
print (divisao) #Resultado ser 1.5;
