x = 3
y = 2
print (x == y) #Espera-se False pois X é maior que Y;
print (x > y) #Espera-se True pois X é maior;
print (x < y) #Espera-se False pois Y é menor;
soma = x + y #Espera-se somar X + Y para dar 5;
print (soma) #Espera-se 5, pois é o resultado de X + Y;
print (soma > y) #Espera-se True, pois é maior;
print (soma <= y) #Espera-se False, pois não é menor e nem igual;
print (soma >= y) #Espera-se True, pois é maior mas não é igual.